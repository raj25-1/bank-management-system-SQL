INSERT INTO transactions (account_id, transaction_type, amount, transaction_date)
SELECT 
    a.account_id,
    'DEPOSIT',
    500,
    DATE_ADD(
        c.created_date,
        INTERVAL FLOOR(RAND() * 180) DAY
    )
FROM accounts a
JOIN customers c 
    ON a.customer_id = c.customer_id;
    
    
